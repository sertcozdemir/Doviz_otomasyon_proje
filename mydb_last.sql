CREATE TRIGGER `after_insert_transaction` AFTER INSERT ON `transactions` FOR EACH ROW BEGIN
    -- Admin veya kullanıcı tarafından yapılan TL yüklemeleri
    IF NEW.transaction_type IN ('admin_deposit', 'deposit') THEN
        UPDATE users
        SET balance = NEW.balance_after
        WHERE user_id = NEW.user_id;

    -- Alış işlemi: TL azalır, döviz artar
    ELSEIF NEW.transaction_type = 'buy' THEN
        UPDATE users
        SET balance = NEW.balance_after
        WHERE user_id = NEW.user_id;

        INSERT INTO wallets (user_id, currency_id, amount)
        VALUES (NEW.user_id, NEW.currency_id, NEW.amount)
        ON DUPLICATE KEY UPDATE amount = amount + NEW.amount;

    -- Satış işlemi: döviz azalır, TL artar
    ELSEIF NEW.transaction_type = 'sell' THEN
        UPDATE users
        SET balance = NEW.balance_after
        WHERE user_id = NEW.user_id;

        UPDATE wallets
        SET amount = amount - NEW.amount
        WHERE user_id = NEW.user_id AND currency_id = NEW.currency_id;
    END IF;
END
$$
